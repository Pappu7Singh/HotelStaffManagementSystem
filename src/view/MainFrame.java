    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        // TODO add your handling code here:
        String name = javax.swing.JOptionPane.showInputDialog(this, "Enter Staff Name:");
        String position = javax.swing.JOptionPane.showInputDialog(this, "Enter Position:");
        String shift = javax.swing.JOptionPane.showInputDialog(this, "Enter Shift (Morning / Day / Night):");
        String contact = javax.swing.JOptionPane.showInputDialog(this, "Enter Contact Number:");

        if (name == null || position == null || shift == null || contact == null) {
            return; // user pressed cancel
        }

        staffController.addStaffAuto(
            name.trim(),
            position.trim(),
            shift.trim(),
            contact.trim(),
            jTable3
        );

        staffController.loadTable(jTable4, staffModel.getRecentStaff());
        updateCounts();
    }//GEN-LAST:event_jButton15ActionPerformed

    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        // TODO add your handling code here:
        int row = jTable3.getSelectedRow();
    if (row < 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Select a staff record to edit.");
        return;
    }

    String name = javax.swing.JOptionPane.showInputDialog(this, "Enter New Name:");
    String position = javax.swing.JOptionPane.showInputDialog(this, "Enter New Position:");
    String shift = javax.swing.JOptionPane.showInputDialog(this, "Enter Shift (Morning / Day / Night):");
    String contact = javax.swing.JOptionPane.showInputDialog(this, "Enter Contact Number:");

    if (name == null || position == null || shift == null || contact == null) {
        return;
    }

    staffController.editStaff(
            jTable3,
            name.trim(),
            position.trim(),
            shift.trim(),
            contact.trim()
    );

    staffController.loadTable(jTable4, staffModel.getRecentStaff());
    updateCounts();
    }//GEN-LAST:event_jButton14ActionPerformed

