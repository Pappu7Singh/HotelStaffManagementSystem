    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        try {
        int id = Integer.parseInt(jTextField1.getText().trim());
        Staff result = staffModel.binarySearchById(id);

        if (result == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "No staff found.");
            return;
        }

        staffController.loadTable(
                jTable2,
                java.util.List.of(result)
        );

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Enter a valid numeric ID.");
        }
    }//GEN-LAST:event_jButton10ActionPerformed

